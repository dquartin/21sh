#include "../shell.h"

void	ft_putstrin(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		ft_putin(str[i]);
		i++;
	}
}

int		ft_putin(int c)
{
	write(0, &c, 1);
	return (c);
}
