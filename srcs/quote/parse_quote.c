#include "../shell.h"

void	parse_quote(char **line)
{
	int		i;

	i = 0;
	while ((*line)[i])
	{
		if ((*line)[i] == '"' || (*line)[i] == '\'')
			(*line)[i] = ' ';
		i++;
	}
}
